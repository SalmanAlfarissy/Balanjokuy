public class Menu {
    private String kode_menu;
    private String namamenu;
    private String stock;
    private String harga_jual;
    private String harga_modal;

    public String getKode_menu() {
        return kode_menu;
    }

    public void setKode_menu(String kode_menu) {
        this.kode_menu = kode_menu;
    }

    public String getNamamenu() {
        return namamenu;
    }

    public void setNamamenu(String namamenu) {
        this.namamenu = namamenu;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public String getHarga_jual() {
        return harga_jual;
    }

    public void setHarga_jual(String harga_jual) {
        this.harga_jual = harga_jual;
    }

    public String getHarga_modal() {
        return harga_modal;
    }

    public void setHarga_modal(String harga_modal) {
        this.harga_modal = harga_modal;
    }
    
    
    
}
